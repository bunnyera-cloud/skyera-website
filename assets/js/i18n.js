function setLang(lang) {
    const selectedLang = translations[lang] ? lang : "tw";

    document.documentElement.lang = selectedLang === "jp" ? "ja" : selectedLang === "en" ? "en" : "zh-Hant";
    document.querySelectorAll("[data-i18n]").forEach(element => {
        const key = element.getAttribute("data-i18n");
        if (translations[selectedLang][key]) {
            element.textContent = translations[selectedLang][key];
        }
    });

    document.querySelectorAll(".lang-switch button").forEach(button => {
        button.classList.toggle("active", button.dataset.lang === selectedLang);
    });

    localStorage.setItem("skyera-lang", selectedLang);
}

document.querySelectorAll(".lang-switch button").forEach(button => {
    button.addEventListener("click", () => setLang(button.dataset.lang));
});
